export interface RgbaColor {
  /**
   * Value of the red channel as a decimal number.
   * Must be between 0-255.
   */
  red: number;

  /**
   * Value of the green channel as a decimal number.
   * Must be between 0-255.
   */
  green: number;

  /**
   * Value of the blue channel as a decimal number.
   * Must be between 0-255.
   */
  blue: number;

  /**
   * Value of the alpha channel as a decimal number.
   * Must be between 0-255.
   */
  alpha: number;
}
